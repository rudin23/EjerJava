public class Impresion
{
   //El método main empieza la ejecución de la plicación en Java
   public static void main (String args[])
   {
      System.out.println("12\t34"); //impresion con println
	  System.out.print("12\t34"); //impresion con print
	  System.out.printf("\n%s\t%s","12","34"); //Impresion con printf
   } //Fin del método main
} //Fin de la clase Impresion